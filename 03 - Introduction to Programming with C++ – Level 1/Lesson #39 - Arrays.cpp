#include <iostream>
using namespace std;

int main()
{
	// An array is a variable that can store multiple values of the same type.
	int x[5] = { 22, 18, 2, 55, 520 };

	cout << x[0] + x[4] << endl; 
	cout << x[1] << endl; // print 18
	cout << x[2] << endl; // print 2
	cout << x[3] << endl; // print 55
	cout << x[4] << endl; // print 520





	int xx[5];

	xx[0] = 10;
	xx[1] = 20;
	
	cout << xx[0] + xx[1] << endl;

	return 0;
}