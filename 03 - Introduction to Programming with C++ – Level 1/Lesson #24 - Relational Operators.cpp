#include <iostream>
using namespace std;

int main()
{
    int A = 10, B = 20;

    cout << (A == B) << endl; // false
    cout << (A != B) << endl; // true
    cout << (A > B) << endl; // false
    cout << (A < B) << endl; // true
    cout << (A >= B) << endl; // false
    cout << (A <= B) << endl; // true

    return 0;
}